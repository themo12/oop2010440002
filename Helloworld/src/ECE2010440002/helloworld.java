package ECE2010440002;

public class helloworld {
/*
 *	1. workspace : 프로젝트의 집합
	2. Java : 프로젝트의 집합
	3. 프로젝트 : Java application을 위해 필요한 모든 정보 저장
	4. class의 이름은 반드시 대문자로 시작
	5. 프로젝트 안의 수많은 class 중에 하나는 반드시 ‘public static void main(String[] args)’을 가져야 함

 */
	public static void main(String[] args) {

		System.out.println("Hello World!");
		// println에서 ln은 line을 뜻함.
		System.out.println("Koo Beom Mo");
		
		int a = 10;
		int b = 20;
		int c;
		c = a + b;
		System.out.println(c);
	}

}
